    /**
     * Outputs a single icon <a> — no <li> wrapper.
     * Menu item title becomes the aria-label/title.
     */
    public function start_el(&$output, $item, $depth = 0, $args = null, $id = 0): void
    {
        $url   = (string) ($item->url ?? '');
        $host  = strtolower((string) (parse_url($url, PHP_URL_HOST) ?: ''));
        $label = $item->title ?: ucfirst(str_replace(['www.', '.com', '.gg', '.net'], '', $host));
        $icon  = $this->resolve_icon($host);

        $output .= sprintf(
            '<a href="%s" target="_blank" rel="noopener noreferrer" aria-label="%s" title="%s">%s</a>',
            esc_url($url),
            esc_attr($label),
            esc_attr($label),
            $icon
        );
    }

    /* No closing tag needed — <a> is self-contained */
    public function end_el(&$output, $item, $depth = 0, $args = null): void {}

    /* Suppress sub-menu wrappers — social menus are always flat */
    public function start_lvl(&$output, $depth = 0, $args = null): void {}
    public function end_lvl(&$output, $depth = 0, $args = null): void {}

    /** Returns the SVG for the first matching hostname pattern. */
    private function resolve_icon(string $host): string
    {
        foreach (self::ICONS as $pattern => $svg) {
            if (str_contains($host, $pattern)) {
                return $svg;
            }
        }
        /* Generic external link icon fallback */
        return '<svg width="18" height="18" fill="none" stroke="currentColor" stroke-width="2" viewBox="0 0 24 24" aria-hidden="true"><path d="M18 13v6a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2V8a2 2 0 0 1 2-2h6"/><polyline points="15 3 21 3 21 9"/><line x1="10" y1="14" x2="21" y2="3"/></svg>';
    }
