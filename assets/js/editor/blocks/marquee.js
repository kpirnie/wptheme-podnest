/**
 * Marquee Strip Block Definition
 *
 * Registers the `podnest/marquee-strip` block in the editor.
 * This is a dynamic (server-side-rendered) block — the editor shows
 * a placeholder; the front end renders via PHP.
 *
 * @module editor/blocks/marquee
 */

import { createPreview } from '../utils/preview.js';

const { registerBlockType } = window.wp.blocks;
const { useBlockProps }     = window.wp.blockEditor;
const { __ }               = window.wp.i18n;
const el                   = window.wp.element.createElement;

/**
 * Registers the Marquee Strip block.
 *
 * @returns {void}
 */
export function registerMarqueeBlock() {
    registerBlockType( 'podnest/marquee-strip', {
        title:       __( 'PodNest: Marquee Strip', 'podnest' ),
        description: __( 'A scrolling strip of feature callouts managed via the Marquee Items CPT.', 'podnest' ),
        icon:        'arrow-right-alt',
        category:    'podnest',
        attributes:  {},

        /**
         * Editor render function — shows a static placeholder.
         *
         * @returns {import('@wordpress/element').WPElement}
         */
        edit() {
            return createPreview(
                useBlockProps(),
                '📢',
                __( 'Marquee Strip', 'podnest' ),
                __( 'Renders all published Marquee Items in a scrolling strip. Manage under PodNest Content → Marquee.', 'podnest' )
            );
        },

        /* Dynamic block — front-end HTML is generated by PHP render callback. */
        save: () => null,
    } );
}
